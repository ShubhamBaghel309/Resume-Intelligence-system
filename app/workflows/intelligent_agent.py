# ============= State Definition =============
class AgentState(TypedDict):
    """State that flows through the agent graph"""

    query: str  # User's original question
    query_analysis: dict  # LLM's analysis of the query
    search_strategy: str  # Chosen strategy: "sql", "vector", "hybrid"
    sql_filters: dict  # Extracted SQL filters
    vector_query: str  # Reformulated query for vector search
    candidate_ids: list  # Resume IDs from SQL filtering
    search_results: list  # Raw search results
    final_results: list  # Deduplicated and enriched results
    answer: str  # Generated natural language answer
    should_retry: bool  # Whether to retry with different strategy
    retry_count: int  # Number of retries attempted
    use_llm_sql: bool  # Flag to use LLM-generated SQL on retry
    llm_generated_sql: str  # SQL query generated by LLM
    aggregation_result: float  # Result from aggregation queries (COUNT, AVG, etc.)
    chat_history: list[dict[str, any]]
    conversation_context: dict  # Candidates from previous conversation (for Q&A queries)


# ============= Query Analysis Models =============
class EntitiesModel(BaseModel):
    """Structured entities extracted from the query"""
    names: list[str] = Field(default_factory=list, description="Candidate names mentioned")
    skills: list[str] = Field(default_factory=list, description="Technical skills mentioned")
    companies: list[str] = Field(default_factory=list, description="Company names mentioned")
    locations: list[str] = Field(default_factory=list, description="Locations mentioned")
    degrees: list[str] = Field(default_factory=list, description="Educational degrees mentioned")
    job_titles: list[str] = Field(default_factory=list, description="Job titles mentioned")
    phone: str | None = Field(default=None, description="Phone number mentioned in query")
    email: str | None = Field(default=None, description="Email address mentioned in query")

class FiltersModel(BaseModel):
    """Structured filters extracted from the query"""
    candidate_ids: list[str] = Field(default_factory=list, description="Specific candidate IDs for follow-up queries")
    min_experience: float | None = Field(default=None, description="Minimum years of experience")
    max_experience: float | None = Field(default=None, description="Maximum years of experience")
    required_skills: list[str] = Field(default_factory=list, description="Required skills")
    location: str | None = Field(default=None, description="Location filter")
    company: str | None = Field(default=None, description="Company filter")
    job_title: str | None = Field(default=None, description="Job title filter")
    current_role: str | None = Field(default=None, description="Current role filter")
    phone: str | None = Field(default=None, description="Filter by phone number")
    email: str | None = Field(default=None, description="Filter by email address")
    institute: str | None = Field(default=None, description="Filter by educational institute/university")
    degree: str | None = Field(default=None, description="Filter by degree type (B.Tech, MBA, etc.)")
    project_keyword: str | None = Field(default=None, description="Filter by project name or technology")

class QueryAnalysis(BaseModel):
    """Structured output from query analyzer"""

    query_type: Literal[
        "name_based",
        "skill_based",
        "experience_based",
        "project_based",
        "education_based",
        "location_based",
        "contact_based",
        "complex_multi_criteria",
    ] = Field(description="Primary type of the query")

    intent: str = Field(
        description="What the user wants to know (e.g., 'find candidates', 'get education details', 'compare candidates')"
    )

    entities: EntitiesModel = Field(
        default_factory=EntitiesModel,
        description="Extracted entities: names, skills, companies, locations, degrees, etc."
    )

    filters: FiltersModel = Field(
        default_factory=FiltersModel,
        description="Structured filters: min_experience, max_experience, etc."
    )

    search_strategy: Literal["sql_first", "vector_first", "hybrid", "sql_only"] = Field(
        description="Recommended search strategy based on query analysis"
    )

    confidence: float = Field(
        description="Confidence in the analysis (0.0 to 1.0)", ge=0.0, le=1.0
    )

    reasoning: str = Field(
        description="Brief explanation of why this strategy was chosen"
    )
    is_refinement: bool = Field(
    description="True if this query refines/filters previous results, False if it's a new search",
    default=False
    )
    
    use_llm_sql: bool = Field(
        description="True if query requires LLM-generated SQL (complex logic, aggregations, counts, comparisons), False if hardcoded filters suffice",
        default=False
    )
    
    sql_complexity_reason: str = Field(
        description="Brief explanation of why LLM SQL is or isn't needed for this query",
        default=""
    )
    
    is_aggregation_query: bool = Field(
        description="True if query asks for COUNT, AVG, SUM, MAX, MIN or other aggregations (should return number/stats, not candidates)",
        default=False
    )
    
    is_qa_query: bool = Field(
        description="True if query asks to analyze/explain/suggest based on previous context WITHOUT needing new search (e.g., 'suggest questions based on his resume', 'explain his projects', 'why is he suitable')",
        default=False
    )


